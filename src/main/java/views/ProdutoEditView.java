    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jTextField1.getText().isEmpty()) {
            this.selectedProduto = null;
            JOptionPane.showMessageDialog(null, "Por favor, informe um ID.", "Campo vazio", JOptionPane.ERROR_MESSAGE);
        } else {
            ProdutoDAO dao = new ProdutoDAO();
            dao.CriaConexao();
            var id = jTextField1.getText();
            var produto = dao.getProdutoById(id);
            dao.FechaConexao();
            if (produto != null) {
                jTextField2.setText(produto.getNome());
                jTextField3.setText(produto.getPreco().toString());
                jTextField4.setText(produto.getQuantidade().toString());
                jTextArea1.setText(produto.getDescricao());
                this.selectedProduto = produto;
            } else {
                this.selectedProduto = null;
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextArea1.setText("");
                JOptionPane.showMessageDialog(null, "Não existe um registro com este ID.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

