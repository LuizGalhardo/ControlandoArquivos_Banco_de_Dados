    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (this.selectedProduto != null) {
            int resposta = JOptionPane.showConfirmDialog(
                    null,
                    "Deseja realmente excluir este produto?",
                    "Confirmação",
                    JOptionPane.YES_NO_OPTION
            );

            if (resposta == JOptionPane.YES_OPTION) {
                ProdutoDAO dao = new ProdutoDAO();
                dao.CriaConexao();
                var id = this.selectedProduto.getId();
                var produto = dao.getProdutoById(String.valueOf(id));

                if (produto != null) {
                    dao.Excluir(produto);
                    dao.FechaConexao();
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextArea1.setText("");
                    this.selectedProduto = null;
                    JOptionPane.showMessageDialog(null, "Produto excluído com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, informe um ID.", "Campo vazio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (this.selectedProduto != null) {
            Produto produto = new Produto();
            ProdutoDAO dao = new ProdutoDAO();
            dao.CriaConexao();
            produto.setId(this.selectedProduto.getId());
            produto.setNome(jTextField2.getText());
            produto.setPreco(Integer.parseInt(jTextField3.getText()));
            produto.setQuantidade(Integer.parseInt(jTextField4.getText()));
            produto.setDescricao(jTextArea1.getText());
            dao.Alterar(produto);
            dao.FechaConexao();
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextArea1.setText("");
            this.selectedProduto = null;
            JOptionPane.showMessageDialog(null, "Os dados do produto foram atualizados.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, informe um ID.", "Campo vazio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    

